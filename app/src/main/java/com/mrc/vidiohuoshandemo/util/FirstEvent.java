package com.mrc.vidiohuoshandemo.util;

public class FirstEvent {
    private final String img;
    private final String name;

    public FirstEvent(String img, String mame) {
        this.img = img;
        this.name = mame;
    }

    public String getImg() {
        return img;
    }

    public String getName() {
        return name;
    }
}
